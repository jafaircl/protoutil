import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

if (typeof Intl.supportedValuesOf !== "function") {
  throw new Error("Intl.supportedValuesOf() is required to regenerate currency codes");
}

const currencyCodes = Intl.supportedValuesOf("currency");
const filePath = resolve(
  dirname(fileURLToPath(import.meta.url)),
  "../src/google/type/currency-codes.ts",
);

const content = `// Generated by \`pnpm --dir packages/core run update:currencies\`.
// Source: \`Intl.supportedValuesOf("currency")\` from the local Node.js runtime.
// Keep this file committed so consumers get a stable compile-time union.

export const CURRENCY_CODES = [
${currencyCodes.map((code) => `  "${code}",`).join("\n")}
] as const;

export type CurrencyCode = (typeof CURRENCY_CODES)[number];

const CURRENCY_CODE_SET = new Set<string>(CURRENCY_CODES);

export function isCurrencyCode(value: string): value is CurrencyCode {
  return CURRENCY_CODE_SET.has(value);
}
`;

writeFileSync(filePath, content);
console.log(`Wrote ${currencyCodes.length} currency codes to ${filePath}`);
