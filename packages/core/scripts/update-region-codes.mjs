import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const packageDir = resolve(scriptDir, "..");
const outputPath = resolve(packageDir, "src/google/type/region-codes.ts");
const TERRITORY_CONTAINMENT_URL =
  "https://raw.githubusercontent.com/unicode-org/cldr-json/main/cldr-json/cldr-core/supplemental/territoryContainment.json";

const REGION_CODE_RE = /^(?:[A-Z]{2}|[0-9]{3})$/;
const response = await fetch(TERRITORY_CONTAINMENT_URL);
if (!response.ok) {
  throw new Error(
    `failed to fetch CLDR territory containment data: ${response.status} ${response.statusText}`,
  );
}

const territoryContainmentJson = await response.json();
const territoryContainment = territoryContainmentJson?.supplemental?.territoryContainment;
if (typeof territoryContainment !== "object" || territoryContainment === null) {
  throw new TypeError("unexpected CLDR territory containment payload");
}

const codes = new Set();
for (const [groupName, groupData] of Object.entries(territoryContainment)) {
  if (groupName.endsWith("-status-deprecated")) {
    continue;
  }
  if (!groupName.includes("-status-")) {
    addRegionCode(codes, groupName);
  }
  if (typeof groupData !== "object" || groupData === null) {
    continue;
  }
  const contains = groupData._contains;
  if (!Array.isArray(contains)) {
    continue;
  }
  for (const code of contains) {
    addRegionCode(codes, code);
  }
}

const sortedCodes = [...codes].sort();

const source = `// Generated by \`moon run core:update-region-codes\`.
// Source: \`${TERRITORY_CONTAINMENT_URL}\`.
// This file is generated from the official Unicode CLDR JSON distribution.
// Keep this file committed so consumers get a stable compile-time region table.

export const REGION_CODES = [
${sortedCodes.map((code) => `  "${code}",`).join("\n")}
] as const;
`;

writeFileSync(outputPath, source);

function addRegionCode(regionCodes, code) {
  if (typeof code !== "string") {
    throw new TypeError(`invalid region code value: ${String(code)}`);
  }
  const normalized = code.toUpperCase();
  if (!REGION_CODE_RE.test(normalized)) {
    throw new TypeError(`invalid region code in CLDR payload: ${code}`);
  }
  regionCodes.add(normalized);
}
