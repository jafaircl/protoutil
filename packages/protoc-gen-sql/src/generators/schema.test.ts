describe("schema generator — sqlserver DDL guards", () => {
  it("sqlserver: table with FK and index wraps CREATE TABLE in IF OBJECT_ID guard and index in IF NOT EXISTS guard", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      message Shelf {
        option (protoutil.sql.v1.message).generate = true;
        string theme = 1;
      }
      message Book {
        option (protoutil.sql.v1.message) = {
          generate: true
          foreign_keys: [{ column: "shelf_id", references_table: "test_v1_shelf", on_delete: FOREIGN_KEY_ACTION_CASCADE }]
        };
        string title = 1 [(protoutil.sql.v1.field).index = true];
      }
    `,
      { engine: "sqlserver" },
    );
    const schema = files.get("schema.sql")!;

    // CREATE TABLE must be guarded with IF OBJECT_ID ... IS NULL / BEGIN / END
    expect(schema).toContain("IF OBJECT_ID(N'test_v1_shelf', N'U') IS NULL");
    expect(schema).toContain("IF OBJECT_ID(N'test_v1_book', N'U') IS NULL");

    // Outer BEGIN/END blocks must be present
    const beginCount = (schema.match(/^BEGIN$/gm) ?? []).length;
    const endCount = (schema.match(/^END$/gm) ?? []).length;
    expect(beginCount).toBe(2);
    expect(endCount).toBe(2);

    // CREATE TABLE inside guard uses double-quotes (not backticks)
    expect(schema).toContain('CREATE TABLE "test_v1_shelf"');
    expect(schema).toContain('CREATE TABLE "test_v1_book"');

    // FK constraint uses double-quoted identifiers
    expect(schema).toContain(
      'CONSTRAINT "fk_test_v1_book_shelf_id" FOREIGN KEY ("shelf_id") REFERENCES "test_v1_shelf" ("id") ON DELETE CASCADE',
    );

    // Indexes use IF NOT EXISTS guard with sys.indexes lookup
    expect(schema).toContain(
      "IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE object_id = OBJECT_ID(N'test_v1_book') AND name = N'idx_test_v1_book_title')",
    );
    expect(schema).toContain(
      "IF NOT EXISTS (SELECT 1 FROM sys.indexes WHERE object_id = OBJECT_ID(N'test_v1_book') AND name = N'idx_test_v1_book_shelf_id')",
    );
    expect(schema).toContain(
      '    CREATE INDEX "idx_test_v1_book_title" ON "test_v1_book" ("title");',
    );
    expect(schema).toContain(
      '    CREATE INDEX "idx_test_v1_book_shelf_id" ON "test_v1_book" ("shelf_id");',
    );
  });

  it("sqlserver: if_not_exists=false emits bare CREATE TABLE and CREATE INDEX without guards", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      message Shelf {
        option (protoutil.sql.v1.message).generate = true;
        string theme = 1 [(protoutil.sql.v1.field).index = true];
      }
    `,
      { engine: "sqlserver", if_not_exists: "false" },
    );
    const schema = files.get("schema.sql")!;

    expect(schema).not.toContain("IF OBJECT_ID");
    expect(schema).not.toContain("BEGIN");
    expect(schema).not.toContain("END");
    expect(schema).not.toContain("sys.indexes");
    expect(schema).toContain('CREATE TABLE "test_v1_shelf"');
    expect(schema).toContain('CREATE INDEX "idx_test_v1_shelf_theme"');
  });

  it("sqlserver: scalar type mapping uses BIGINT IDENTITY and DATETIMEOFFSET", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      message Types {
        option (protoutil.sql.v1.message).generate = true;
        bool   f_bool   = 1;
        int32  f_int32  = 2;
        int64  f_int64  = 3;
        string f_string = 4;
      }
    `,
      { engine: "sqlserver" },
    );
    const schema = files.get("schema.sql")!;

    expect(schema).toContain('"id" BIGINT IDENTITY(1,1) NOT NULL');
    expect(schema).toContain('"f_bool" BIT NOT NULL');
    expect(schema).toContain('"f_int32" INT NOT NULL');
    expect(schema).toContain('"f_int64" BIGINT NOT NULL');
    expect(schema).toContain('"f_string" NVARCHAR(MAX) NOT NULL');
    expect(schema).toContain('"created_at" DATETIMEOFFSET NOT NULL DEFAULT SYSDATETIMEOFFSET()');
  });
});

