describe("schema generator — ENUM_STRATEGY_CHECK_CONSTRAINT quoting", () => {
  it("mysql: CHECK constraint uses backtick-quoted column name", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      enum Status { STATUS_UNSPECIFIED = 0; STATUS_ACTIVE = 1; STATUS_INACTIVE = 2; }
      message Book {
        option (protoutil.sql.v1.message).generate = true;
        Status status = 1 [(protoutil.sql.v1.field).enum_strategy = ENUM_STRATEGY_CHECK_CONSTRAINT];
      }
    `,
      { engine: "mysql" },
    );
    const schema = files.get("schema.sql")!;
    // MySQL must use backtick, not double-quote, in the CHECK expression
    expect(schema).toContain(
      "TEXT CHECK (`status` IN ('STATUS_UNSPECIFIED', 'STATUS_ACTIVE', 'STATUS_INACTIVE')) NOT NULL",
    );
    expect(schema).not.toContain('CHECK ("status"');
  });

  it("sqlserver: CHECK constraint uses double-quoted column name", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      enum Status { STATUS_UNSPECIFIED = 0; STATUS_ACTIVE = 1; STATUS_INACTIVE = 2; }
      message Book {
        option (protoutil.sql.v1.message).generate = true;
        Status status = 1 [(protoutil.sql.v1.field).enum_strategy = ENUM_STRATEGY_CHECK_CONSTRAINT];
      }
    `,
      { engine: "sqlserver" },
    );
    const schema = files.get("schema.sql")!;
    expect(schema).toContain(
      "TEXT CHECK (\"status\" IN ('STATUS_UNSPECIFIED', 'STATUS_ACTIVE', 'STATUS_INACTIVE')) NOT NULL",
    );
  });

  it("CHECK constraint uses resolved column_name override, not proto field name", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      enum Status { STATUS_UNSPECIFIED = 0; STATUS_ACTIVE = 1; }
      message Book {
        option (protoutil.sql.v1.message).generate = true;
        Status status = 1 [
          (protoutil.sql.v1.field).enum_strategy = ENUM_STRATEGY_CHECK_CONSTRAINT,
          (protoutil.sql.v1.field).column_name = "book_status"
        ];
      }
    `,
      { engine: "postgres" },
    );
    const schema = files.get("schema.sql")!;
    // CHECK should reference "book_status", not the proto field name "status"
    expect(schema).toContain('"book_status" TEXT CHECK ("book_status" IN');
    expect(schema).not.toContain('CHECK ("status"');
  });
});
