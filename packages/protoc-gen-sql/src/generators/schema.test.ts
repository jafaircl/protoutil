describe("schema generator — IF NOT EXISTS option", () => {
  it("default (if_not_exists unset) produces IF NOT EXISTS", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      message Book { option (protoutil.sql.v1.message).generate = true; string title = 1; }
    `,
      { engine: "postgres" },
    );
    const schema = files.get("schema.sql")!;
    expect(schema).toContain("CREATE TABLE IF NOT EXISTS");
  });

  it("if_not_exists=true explicitly produces IF NOT EXISTS", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      message Book { option (protoutil.sql.v1.message).generate = true; string title = 1 [(protoutil.sql.v1.field).index = true]; }
    `,
      { engine: "postgres", if_not_exists: "true" },
    );
    const schema = files.get("schema.sql")!;
    expect(schema).toContain("CREATE TABLE IF NOT EXISTS");
    expect(schema).toContain("CREATE INDEX IF NOT EXISTS");
  });

  it("if_not_exists=false omits IF NOT EXISTS from CREATE TABLE", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      message Book { option (protoutil.sql.v1.message).generate = true; string title = 1; }
    `,
      { engine: "postgres", if_not_exists: "false" },
    );
    const schema = files.get("schema.sql")!;
    expect(schema).toContain('CREATE TABLE "test_v1_book"');
    expect(schema).not.toContain("IF NOT EXISTS");
  });

  it("if_not_exists=false omits IF NOT EXISTS from all CREATE INDEX statements", async () => {
    const files = await generate(
      `
      syntax = "proto3"; package test.v1; import "protoutil/sql/v1/options.proto";
      message Book {
        option (protoutil.sql.v1.message) = { generate: true, indexes: ["author"] };
        string author = 1 [(protoutil.sql.v1.field).index = true];
      }
    `,
      { engine: "postgres", if_not_exists: "false" },
    );
    const schema = files.get("schema.sql")!;
    expect(schema).not.toContain("IF NOT EXISTS");
    expect(schema).toContain('CREATE INDEX "idx_test_v1_book_author"');
  });
});

